/* Declara dos variables numéricas (con el valor que desees),
  muestra por consola la suma, resta,
  multiplicación, división y módulo (resto de la división). */

fun main(){
    var n1 = 2
    var n2 = 1
    println("La suma de $n1 + $n2 es ${n1+n2}")
    println("La resta de $n1 - $n2 es ${n1-n2}")
    println("La multiplicacion de $n1 * $n2 es ${n1*n2}")
    println("La division de $n1 / $n2 es ${n1/n2}")
    println("El modulo de $n1 / $n2 es ${n1%n2}")
}



